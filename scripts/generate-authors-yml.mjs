#!/usr/bin/env node
/**
 * Build-time generator for `authors.yml` (the Docusaurus authorsMap).
 *
 * `src/data/people.json` is the canonical, hand-edited registry for every
 * person referenced on the site — community-meeting speakers, blog
 * authors, maintainers, profile-page subjects. The blog and community
 * plugins read `authors.yml` directly (see `authorsMapPath` in
 * `docusaurus.config.ts`), and Docusaurus expects a flat map shape:
 *
 *   <key>:
 *     name: ...
 *     title: ...
 *     url: ...
 *     image_url: ...
 *
 * So we project the subset of `people.json` entries that have an
 * `authors_key` into that shape. Entries without `authors_key` (speakers
 * who have never authored a post, etc.) are skipped — they don't need a
 * byline.
 *
 * Idempotent. Safe to run before every build (wired into package.json's
 * `prestart` and `prebuild`). Output is sorted by key for stable diffs.
 */
import { readFile, writeFile } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const PEOPLE_PATH = join(REPO_ROOT, 'src', 'data', 'people.json');
const OUT_PATH = join(REPO_ROOT, 'authors.yml');

const HEADER = [
  '# DO NOT EDIT — generated by scripts/generate-authors-yml.mjs from',
  '# src/data/people.json. Update the canonical entry there, then run',
  '# `npm run generate:authors-yml` (also runs automatically before',
  '# `npm start` and `npm run build`).',
  '',
].join('\n');

// YAML keys never need quoting here (authors keys are simple identifiers
// and field names are plain ASCII), but string values can contain
// characters that YAML would otherwise interpret — quote them defensively.
function yamlString(s) {
  // Single-quote the value and escape any embedded single quotes per YAML
  // single-quoted-string rules (double a single quote to escape it).
  return `'${String(s).replace(/'/g, "''")}'`;
}

async function main() {
  const raw = await readFile(PEOPLE_PATH, 'utf8');
  const data = JSON.parse(raw);
  if (!Array.isArray(data?.people)) {
    throw new Error(`people.json is missing a top-level "people" array`);
  }

  // Only entries with an `authors_key` map into authors.yml. Skip the rest
  // silently — speaker-only entries (community guests, external panelists)
  // don't need bylines.
  const withKey = data.people
    .filter((p) => typeof p.authors_key === 'string' && p.authors_key.length > 0)
    .sort((a, b) => a.authors_key.localeCompare(b.authors_key));

  const seen = new Set();
  const lines = [HEADER];
  for (const p of withKey) {
    if (seen.has(p.authors_key)) {
      throw new Error(
        `Duplicate authors_key ${JSON.stringify(p.authors_key)} in people.json ` +
          `(slugs: ${data.people
            .filter((q) => q.authors_key === p.authors_key)
            .map((q) => q.slug)
            .join(', ')})`,
      );
    }
    seen.add(p.authors_key);

    if (!p.name) {
      throw new Error(`Missing name for ${p.authors_key} in people.json`);
    }

    lines.push(`${p.authors_key}:`);
    lines.push(`  name: ${yamlString(p.name)}`);
    if (p.byline_title) lines.push(`  title: ${yamlString(p.byline_title)}`);
    if (p.url) lines.push(`  url: ${yamlString(p.url)}`);
    if (p.image_url) lines.push(`  image_url: ${yamlString(p.image_url)}`);
    lines.push('');
  }

  await writeFile(OUT_PATH, lines.join('\n'));

  console.log(
    `[authors-yml] projected ${withKey.length} people.json entries → ${OUT_PATH.replace(
      REPO_ROOT + '/',
      '',
    )}`,
  );
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
