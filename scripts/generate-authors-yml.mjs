#!/usr/bin/env node
/**
 * Build-time generator for `authors.yml` (the Docusaurus authorsMap).
 *
 * `src/data/people.json` is the canonical, hand-edited registry for every
 * person referenced on the site — community-meeting speakers, blog
 * authors, maintainers, profile-page subjects. The blog and community
 * plugins read `authors.yml` directly (see `authorsMapPath` in
 * `docusaurus.config.ts`), and Docusaurus expects a flat map shape:
 *
 *   <key>:
 *     name: ...
 *     title: ...
 *     url: ...
 *     image_url: ...
 *
 * So we project the subset of `people.json` entries that have an
 * `authors_key` into that shape. Entries without `authors_key` (speakers
 * who have never authored a post, etc.) are skipped — they don't need a
 * byline.
 *
 * Idempotent. Safe to run before every build (wired into package.json's
 * `prestart` and `prebuild`). Output is sorted by key for stable diffs.
 */
import { readFile, writeFile } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const REPO_ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');
const PEOPLE_PATH = join(REPO_ROOT, 'src', 'data', 'people.json');
const OUT_PATH = join(REPO_ROOT, 'authors.yml');

const HEADER = [
  '# DO NOT EDIT — generated by scripts/generate-authors-yml.mjs from',
  '# src/data/people.json. Update the canonical entry there, then run',
  '# `npm run generate:authors-yml` (also runs automatically before',
  '# `npm start` and `npm run build`).',
  '',
].join('\n');

// YAML keys never need quoting here (authors keys are simple identifiers
// and field names are plain ASCII), but string values can contain
// characters that YAML would otherwise interpret — quote them defensively.
function yamlString(s) {
  // Single-quote the value and escape any embedded single quotes per YAML
  // single-quoted-string rules (double a single quote to escape it).
  return `'${String(s).replace(/'/g, "''")}'`;
}

// Classify a `same_as` URL into a Docusaurus authors.yml `socials:` platform
// key. Keep this aligned with src/theme/Blog/Components/Author/Socials/index.tsx's
// SocialPlatformConfigs (which knows the icon set) and with the hosts already
// handled by src/components/PersonPage/index.tsx#platformLabel — both surfaces
// should classify the same URL the same way. Unknown hosts fall through to
// `website` so they still surface as a generic chip rather than getting
// silently dropped.
function classifySameAs(url) {
  let host;
  try {
    host = new URL(url).hostname.replace(/^www\./, '').toLowerCase();
  } catch {
    return null;
  }
  if (host === 'github.com') return 'github';
  if (host === 'linkedin.com') return 'linkedin';
  if (host === 'x.com' || host === 'twitter.com') return 'x';
  if (host === 'bsky.app' || host.endsWith('.bsky.app')) return 'bluesky';
  // Treat any Mastodon-style instance as `mastodon` — Docusaurus's classic
  // Author swizzle picks the icon by platform key, not by host, so several
  // instances all map to one key here.
  if (
    host === 'mastodon.social' ||
    host === 'hachyderm.io' ||
    host.endsWith('.hachyderm.io') ||
    host.endsWith('.mastodon.social')
  )
    return 'mastodon';
  return 'website';
}

async function main() {
  const raw = await readFile(PEOPLE_PATH, 'utf8');
  const data = JSON.parse(raw);
  if (!Array.isArray(data?.people)) {
    throw new Error(`people.json is missing a top-level "people" array`);
  }

  // Only entries with an `authors_key` map into authors.yml. Skip the rest
  // silently — speaker-only entries (community guests, external panelists)
  // don't need bylines.
  const withKey = data.people
    .filter((p) => typeof p.authors_key === 'string' && p.authors_key.length > 0)
    .sort((a, b) => a.authors_key.localeCompare(b.authors_key));

  const seen = new Set();
  const lines = [HEADER];
  for (const p of withKey) {
    if (seen.has(p.authors_key)) {
      throw new Error(
        `Duplicate authors_key ${JSON.stringify(p.authors_key)} in people.json ` +
          `(slugs: ${data.people
            .filter((q) => q.authors_key === p.authors_key)
            .map((q) => q.slug)
            .join(', ')})`,
      );
    }
    seen.add(p.authors_key);

    if (!p.name) {
      throw new Error(`Missing name for ${p.authors_key} in people.json`);
    }

    lines.push(`${p.authors_key}:`);
    lines.push(`  name: ${yamlString(p.name)}`);
    if (p.byline_title) lines.push(`  title: ${yamlString(p.byline_title)}`);
    if (p.url) lines.push(`  url: ${yamlString(p.url)}`);
    if (p.image_url) lines.push(`  image_url: ${yamlString(p.image_url)}`);
    // Project the canonical `same_as` array (plus the top-level `url`
    // when it's classifiable as a social platform) into a Docusaurus
    // authors.yml `socials:` block keyed by platform. This is the one
    // input the existing BlogAuthor swizzle reads for social-icon
    // chips, AND is what buildAuthors() in blog-post-schema.tsx
    // promotes to Article.author.sameAs.
    //
    // Including `url` here is what makes the blog byline's chip list
    // match the /people/<slug>/ profile page's chip list (the
    // PersonPage builds its chip set from `{url} ∪ same_as` then
    // filters by host). For maintainers whose `url` is a GitHub
    // profile, this gets the GitHub chip into the byline.
    //
    // Without this projection, authors without a /people/<slug>/
    // profile page would emit blog Person JSON-LD with no sameAs at
    // all — Google's audit flag.
    {
      // De-dup by platform — first URL per platform wins. `url` is
      // considered first so the canonical GitHub-style profile takes
      // precedence over any same_as duplicate. people.json may carry
      // e.g. two Mastodon instances; the second silently loses (rare
      // and not worth a schema change).
      const seenPlatform = new Set();
      const socialLines = [];
      const candidates = [
        ...(typeof p.url === 'string' ? [p.url] : []),
        ...(Array.isArray(p.same_as) ? p.same_as : []),
      ];
      for (const url of candidates) {
        if (typeof url !== 'string') continue;
        const platform = classifySameAs(url);
        if (!platform || seenPlatform.has(platform)) continue;
        seenPlatform.add(platform);
        socialLines.push(`    ${platform}: ${yamlString(url)}`);
      }
      if (socialLines.length > 0) {
        lines.push('  socials:');
        lines.push(...socialLines);
      }
    }
    lines.push('');
  }

  await writeFile(OUT_PATH, lines.join('\n'));

  console.log(
    `[authors-yml] projected ${withKey.length} people.json entries → ${OUT_PATH.replace(
      REPO_ROOT + '/',
      '',
    )}`,
  );
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
